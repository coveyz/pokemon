const question = `
给你一个整数数组 nums 和一个整数 k ，请你返回子数组内所有元素的乘积严格小于 k 的连续子数组的数目。
`;

/**
 * @param {number[]} nums
 * @param {number} k
 * @return {number}
 */
var numSubarrayProductLessThanK = function (nums, k) {
	let left = 0,
		right = 1;
	let count = 0;

	while (left < nums.length) {
		// console.log('cur_', nums[left]);
		if (nums[left] < k) count++;

		let tmpResult = nums[left];

		while (right < nums.length) {
			// console.log('xxx=>', nums[right]);
			if (tmpResult * nums[right] < k) {
				tmpResult = tmpResult * nums[right];
				count++;
				right++;
			} else {
				break;
			}
		}

		left++;
		right = left + 1;
	}

	return count;
};

